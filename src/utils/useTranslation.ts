const TRANSLATIONS: Record<string, Record<string, string>> = { en: EN, fr: FR, de: DE, es: ES, it: IT, pt: PT, ja: JA }

let _lang: string | null = null

const SUPPORTED_LANGS = ['en', 'fr', 'de', 'es', 'it', 'pt', 'ja']

function detectLang(): string {
  if (typeof document === 'undefined') return 'en'
  const lang = document.documentElement.lang?.toLowerCase() || 'en'
  // Exact match first (e.g. 'fr', 'de')
  if (SUPPORTED_LANGS.includes(lang)) return lang
  // Prefix match (e.g. 'fr-FR' -> 'fr', 'pt-BR' -> 'pt')
  const prefix = lang.split('-')[0]
  if (SUPPORTED_LANGS.includes(prefix)) return prefix
  return 'en'
}

export function useAupT(): (key: string, vars?: Record<string, string | number>) => string {
  if (!_lang) _lang = detectLang()
  const strings = TRANSLATIONS[_lang] || EN

  return (key: string, vars?: Record<string, string | number>) => {
    let value = strings[key] || EN[key] || key
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        value = value.replace(`{{${k}}}`, String(v))
      }
    }
    return value
  }
}
